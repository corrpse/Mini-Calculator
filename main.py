a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))

print("\nВыберите операцию:")
print("1. + (сложение)")
print("2. - (вычитание)")
print("3. * (умножение)")
print("4. / (деление)")

c = input("Введите номер операции: ")

if c == "1":
    print("Результат:", a + b)
elif c == "2":
    print("Результат:", a - b)
elif c == "3":
    print("Результат:", a * b)
elif c == "4":
    print("Результат:", a / b)
else:
    print("Такой операции нет.")